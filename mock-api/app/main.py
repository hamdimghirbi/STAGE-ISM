@mcp.tool()
async def login(email: str, password: str) -> str:
    """
    Login to Portalite and get JWT token. Always call this first.
    from_openapi does not support OAuth2 form data — this tool sends the correct
    form data format and stores the token so DynamicAuthTransport injects it
    automatically into all subsequent requests.
    """
    async with httpx.AsyncClient(
        transport=httpx.ASGITransport(app=app),
        base_url="http://fastapi"
    ) as client:
        resp = await client.post(
            "/api/auth/login",
            data={"username": email, "password": password}
        )
        result = resp.json()
        _token["value"] = result["access_token"]
        return json.dumps(result)


@mcp.tool()
async def create_expense_with_receipt(
    expense_type: str,
    month: str,
    total_amount: float,
    description: str,
    image_base64: str,
    image_mime_type: str = "image/jpeg",
    billable_to_client: bool = False,
    comment: str = ""
) -> str:
    """
    Create an expense report with a receipt image sent by the user in the conversation.
    When the user uploads or pastes an image (ticket, receipt, invoice), automatically:
    1. Read the image from the conversation
    2. Encode it to base64
    3. Call this tool with the base64 data

    DO NOT ask the user to encode the image manually.
    DO NOT use file paths.
    expense_type: Restaurant, Titre de transport, Materiel, Telephonie, Teletravail, Autre.
    month: YYYY-MM format.
    image_base64: encode the image from the conversation to base64 and pass it here.
    image_mime_type: image/jpeg, image/png or application/pdf.
    """
    try:
        image_bytes = base64.b64decode(image_base64)
        ext = image_mime_type.split("/")[-1]
        filename = f"receipt.{ext}"
        async with httpx.AsyncClient(
            transport=httpx.ASGITransport(app=app),
            base_url="http://fastapi",
            headers={"Authorization": f"Bearer {_token['value']}"}
        ) as client:
            resp = await client.post(
                "/api/expenses",
                data={
                    "type": expense_type,
                    "month": month,
                    "total_amount": str(total_amount),
                    "description": description,
                    "billable_to_client": str(billable_to_client).lower(),
                    "comment": comment
                },
                files={"receipt": (filename, image_bytes, image_mime_type)}
            )
            return json.dumps(resp.json())
    except Exception as e:
        return json.dumps({"error": str(e)})


@mcp.tool()
async def extract_expense(
    image_base64: str,
    month: str,
    image_mime_type: str = "image/jpeg"
) -> str:
    """
    Extract expense data from a receipt image and create the expense automatically.
    When the user sends a ticket or receipt image in the conversation:
    1. Encode the image to base64
    2. Call this tool — it uses Claude Vision to extract the data
    3. Then creates the expense with the receipt attached
    DO NOT ask the user for the data manually — extract it from the image.
    image_base64: the image from the conversation encoded in base64.
    month: YYYY-MM format.
    """
    async with httpx.AsyncClient(
        transport=httpx.ASGITransport(app=app),
        base_url="http://fastapi",
        headers={"Authorization": f"Bearer {_token['value']}"}
    ) as client:
        resp = await client.post(
            "/api/extract-expense",
            json={
                "image_base64": image_base64,
                "image_mime_type": image_mime_type,
                "month": month
            }
        )
        return json.dumps(resp.json())


anthropic_client = Anthropic()

@app.post("/api/extract-expense", tags=["meta"])
async def extract_expense(
    payload: dict = Body(...),
    current: User = Depends(get_current_user)
):
    """Extract expense data from a receipt image using Claude Vision and create the expense."""
    image_base64 = payload.get("image_base64")
    image_mime_type = payload.get("image_mime_type", "image/jpeg")
    month = payload.get("month")

    # Call Claude Vision to extract data
    response = anthropic_client.messages.create(
        model="claude-opus-4-5",
        max_tokens=500,
        messages=[{
            "role": "user",
            "content": [
                {
                    "type": "image",
                    "source": {
                        "type": "base64",
                        "media_type": image_mime_type,
                        "data": image_base64
                    }
                },
                {
                    "type": "text",
                    "text": "Extrait les données de ce ticket de caisse. Retourne UNIQUEMENT un JSON avec: type (Restaurant/Titre de transport/Materiel/Telephonie/Teletravail/Autre), total_amount (float), description (string), comment (string)."
                }
            ]
        }]
    )

    import re
    text = response.content[0].text
    match = re.search(r'\{.*\}', text, re.DOTALL)
    data = json.loads(match.group())

    return {
        "extracted": data,
        "month": month
    }


