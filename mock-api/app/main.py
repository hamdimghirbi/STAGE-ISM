# Token storage — cannot use a simple variable in async, dict content can be modified
_token = {"value": ""}


class DynamicAuthTransport(httpx.AsyncBaseTransport):
    """
    Custom HTTP transport that injects the JWT token automatically into every request.
    Wraps httpx.ASGITransport to intercept requests before they reach the FastAPI app.
    Without this, all tools generated by from_openapi would return 401 Unauthorized.
    """
    def __init__(self, app):
        """Wrap the FastAPI ASGI app so requests go directly without network."""
        self._transport = httpx.ASGITransport(app=app)

    async def handle_async_request(self, request):
        """Intercept every request and inject the Bearer token if one is stored."""
        if _token["value"]:
            request.headers["Authorization"] = f"Bearer {_token['value']}"
        return await self._transport.handle_async_request(request)


# Patch OpenAPITool to wrap results in TextContent
original_execute = OpenAPITool._execute_request

async def patched_execute_request(self, *args, **kwargs):
    """
    Patch for OpenAPITool._execute_request.
    By default, tools return raw dict or list — MCP expects TextContent format.
    This patch converts dict/list results to JSON string to fix 208 validation errors.
    Must be applied before creating the MCP server with from_openapi.
    """
    result = await original_execute(self, *args, **kwargs)
    if isinstance(result, (dict, list)):
        return json.dumps(result)
    return result

OpenAPITool._execute_request = patched_execute_request


